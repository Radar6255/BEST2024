x = 5
y = x + 1

# Printing current state of variables
print("X is " + str(x))
print("Y is " + str(y))
print()

z = x + y
print("X + Y = " + str(z))

z = x - y
print("X - Y = " + str(z))

z = x * y
print("X * Y = " + str(z))

z = x / y
print("X / Y = " + str(z))
